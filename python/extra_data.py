# dictionary mapping each braille expression to its english character
# leaves all the NULL mappings as NULL, this way we can add more characters in the future 

# Reorder the english combinations so that they match the order of the braille characters, unmapped braille characters are mapped to NULL
english_indices=[[None,None], [16,None], [None,None], [6, 33], [None,None], [15,None], [None,None], [5, 32], [24,None], [13,None], [None,None], [3, 30], 
                 [23,None], [12,None], [None,None], [2, 29], [None,None], [17,None], [None,None], [7, 34], [21,None], [11,None], [46,None], [1, 28], 
                 [25,None], [14, 45], [None,None], [4, 31], [20,None], [10,None], [None,None], [0, 27], [None,None], [19,None], [22,None], [9, 26], 
                 [None,None], [18,None], [44,None], [8, 35], [51,None], [47,None], [None,None], [None,None], [None,None], [43,None], [50,None], 
                 [None,None], [None,None], [39,None], [36,None], [40,None], [38,None], [41,None], [None,None], [37,None], [None,None], [None,None], 
                 [None,None], [None,None], [42,None], [None,None], [49,None], [48,None]]

if len(braille) != len(english_indices):
    raise ValueError("Mismatch between Braille combinations and English indices length.")

# Each braille character maps to a list of two things, either those are english characters or NULL. If both are not-NULL, then first will always be a letter and second will be a digit
# Only outlier to this is the braille: O..OO. which maps to letter 'o' and to character '>' which is not a digit
braille_english_dict = {}
for index, character in enumerate(braille):
    value0 = (english[english_indices[index][0]] if english_indices[index][0] is not None else 'NULL')
    value1 = (english[english_indices[index][1]] if english_indices[index][1] is not None else 'NULL')
    braille_english_dict[character] = [value0, value1]

# Print the dictionary
for key, value in braille_english_dict.items():
    print(f"{key}: {value}")