function isBraille(inputVal) {
    if (inputVal.length % 6 !== 0) {
        return false;
    }
    for (let i = 0; i < inputVal.length; i += 6) {
        const splitInput = inputVal.slice(i, i + 6);
        if (splitInput.length !== 6) {
            return false;
        }
        for (const char of splitInput) {
            if (char !== 'O' && char !== '.') {
                return false;
            }
        }
    }
    return true;
}

